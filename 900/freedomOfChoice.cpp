#include<iostream>
#include<vector>
using namespace std;
 
int main()
{
    ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    int t, m, n, l, r;
    cin >> t;
    while (t--)
    {
        cin >> m;
        while (m--)
        {
            cin >> n >> l >> r;
            vector<long long int> a(n), c(n);
            for (long long int& d : a) cin >> d;
            for (long long int& d : c) cin >> d;
            
        }
        
    }
    
}